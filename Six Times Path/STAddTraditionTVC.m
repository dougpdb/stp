-(IBAction)save:(id)sender
{
    NSLog(@"Tell STAddTraditionTVC delegate that Save was tapped on the STAddTraditionTVC.");
    NSLog(@"The text in _traditionNameTextField is: %@", _traditionNameTextField.text);
    NSLog(@"This is what's in the text field: %@", _traditionNameTextField.text);
    
    SpiritualTradtion *tradition = [NSEntityDescription insertNewObjectForEntityForName:@"SpiritualTradition" inManagedObjectContext:self.managedObjectContext];
    
    tradition.name = self.traditionNameTextField.text;
    
    
    [self.managedObjectContext save:nil];   // write to database

    [self.delegate saveWasTappedOnAddTradition:self];
}


